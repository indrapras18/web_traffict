function getDataFromFirebase($path) {
    $firebaseUrl = "https://esp-firebase-demo-20e4d-default-rtdb.asia-southeast1.firebasedatabase.app/";
    $url = "{$firebaseUrl}{$path}.json";

    $ch = curl_init($url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);

    $response = curl_exec($ch);

    if (curl_errno($ch)) {
        echo 'Error:' . curl_error($ch);
        return null;
    }

    curl_close($ch);

    return json_decode($response, true);
}

